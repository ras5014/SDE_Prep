import math

# Python does decimal division by default
print(5 / 2)  # 2.5

# To perform integer division, use the // operator
print(5 // 2)  # 2

# By default, Python rounds down when performing integer division
print(5 // 3)  # 1, but 5 / 3 is 1.6666666666666667


# To round up to the nearest integer
print(math.ceil(5 / 3))  # 2

# To round down to the nearest integer
print(math.floor(5 / 3))  # 1

# To calculate square root
print(math.sqrt(5))  # 2.23606797749979

# To calculate the power
print(math.pow(2, 3))  # 8.0

# To calculate absolute value
print(abs(-5))  # 5
